/*
 ciclo for


for(inicializacion) ; condicion; aumento o decremento) {
intruccion;
}
 */
package Bucles;


public class TeoriaBucles3 {
    public static void main(String[] args) {
       
        for (int i=1; i<=10; i++){
            System.out.println(i);
        }
    }
    
}

